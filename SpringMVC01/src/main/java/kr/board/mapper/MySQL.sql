-- BOARD TABLE 생성 --
-- 실행 드래그 ALT+X
CREATE TABLE BOARD(
	IDX INT NOT NULL AUTO_INCREMENT, -- ORACLE의 시퀀스 같은 역할 자동으로 1씩 증가
	TITLE VARCHAR(100) NOT NULL,
	CONTENTS VARCHAR(2000) NOT NULL,
	WRITER VARCHAR(30) NOT NULL,
	INDATE DATETIME DEFAULT NOW(),
	COUNT INT DEFAULT 0,
	
	PRIMARY KEY(IDX)
);

